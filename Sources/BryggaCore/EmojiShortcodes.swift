	/// Look up an exact shortcode (case-insensitive). Returns `nil` when
	/// unknown.
	public static func emoji(for shortcode: String) -> String? {
		map[shortcode.lowercased()]
	}

	/// Shortcodes starting with `prefix`, sorted alphabetically. Used by
	/// Tab completion in `InputBar`.
	public static func matches(prefix: String) -> [String] {
		let p = prefix.lowercased()
		guard !p.isEmpty else { return [] }
		return map.keys.filter { $0.hasPrefix(p) }.sorted()
	}
}
